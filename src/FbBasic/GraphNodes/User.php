namespace FbBasic\GraphNodes;

use Facebook\GraphNodes\GraphUser;

class User extends GraphUser implements GraphInterface
{
    protected static $graphObjectFields = [
        "id",
        "name"
    ];

    /**
     * Getter for $graphObjectFields.
     *
     * @return array
